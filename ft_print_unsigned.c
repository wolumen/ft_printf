#include "ft_printf.h"

int	ft_print_unsigned(unsigned int n)
{
	int		print_length;
	char	*num;

	print_length = 0;
	if (n == 0)
		print_length += write(1, "0", 1);
	else
	{
		num = ft_uitoa(n);						// gibt str zurück
		print_length += ft_printstr(num);		// gibt int zurück und printed
		free(num);
	}
	return (print_length);
}

char	*ft_uitoa(unsigned int n)
{
	char	*num;
	int		len;

	len = ft_num_len(n);
	num = (char *)malloc(sizeof(char) * (len + 1));
	if (!num)
		return (0);
	num[len] = '\0';
	while (n != 0)
	{
		num[len - 1] = n % 10 + 48;				// STEFFEN woher weiß PC hier 10 + 48 unterschiedlich zubehandeln?
		n = n / 10;
		len--;									// wieder von hinten nach vorn drucken
	}
	return (num);
}

int	ft_num_len(unsigned	int num)
{
	int	len;

	len = 0;
	while (num != 0)
	{
		len++;
		num = num / 10;
	}
	return (len);
}